import mongoose, { Schema, models } from "mongoose";

const userSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const RegisterUser = mongoose.models.RegisterUser || mongoose.model("RegisterUser", userSchema);
export default RegisterUser;